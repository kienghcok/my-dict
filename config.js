function derivePhonology(status) {
    if (!status || status === "/" || status === "nan") return "";
    try {
        // 放寬正則：抓取前 5 位核心內容 + 剩餘備註
        const match = status.match(/^([一-龥])(開|合|齊|撮)?([一二三四])?([一-龥])([平上去入])(.*)$/);
        if (!match) return status;

        let [_, initial, mouth, grade, rime, tone, extra] = match;
        mouth = mouth || "開";

        // 濁音列表（用於標註陽平聲調）
        const voicedList = ["明", "微", "泥", "來", "日", "孃", "邪", "禪", "疑", "匣", "喩", "並", "定", "澄", "從", "牀", "群"];
        const isVoiced = voicedList.includes(initial);
        const toneKey = (tone === "平" && isVoiced) ? "濁平" : (tone === "平" ? "清平" : tone);

        // 1. 推導聲母
        let resInitial = PHONOLOGY_RULES.initials[initial];
        if (typeof resInitial === "function") resInitial = resInitial(tone);

        // 2. 推導韻母
        let resFinal = "";
        const rimeGroup = PHONOLOGY_RULES.finals[rime] || PHONOLOGY_RULES.finals[rime[0]]; // 兼容董/送等同韻部
        if (rimeGroup) {
            const rule = rimeGroup[mouth];
            resFinal = (typeof rule === "function") ? rule(status) : (rule || "");
        }

        // 3. 獲取聲調
        const resTone = PHONOLOGY_RULES.tones[toneKey] || "";

        return `${resInitial || ""}${resFinal}${resTone}${extra || ""}`;
    } catch (e) {
        return status;
    }
}
